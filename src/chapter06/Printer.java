package chapter06;

public class Printer {
	void println(int x) {
		int result = x;
		System.out.println(x);
	}
	
	void println(boolean x) {
		boolean result = x;
		System.out.println(x);
	}
	
	void println(double x) {
		double result = x;
		System.out.println(x);
	}

	public void println(String string) {
		System.out.println(string);
		
	}
    
	
}
